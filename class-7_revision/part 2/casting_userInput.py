# type casting/conversion

# a way to convert one type of data into another.

# int
num = 48.3
#num = int(num)
print(int(num))
print(int(True))
print(int(False))
# str -> int | possibly only if the str contains an int values inside
print(int("45"))


# float
print(float(45.564))
print(float(456))
print(float(True))
print(float(False))
# str -> float | possibly only if the str contains an int or float values inside
print(float("45.89"))
print(float("89"))


# bool
print(bool(0))
print(bool(-1))
print(bool(6))

print(bool(""))
print(bool(" "))





